    private void jButton29ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton29ActionPerformed
        String bal = jTextField6.getText(); 
        String cos = jTextField7.getText();
        double balance = Double.parseDouble(bal);
        double cost = Double.parseDouble(cos);
        
        if (balance < cost)
        {
            JOptionPane.showMessageDialog(null, "THERE ARE INSUFFICIENT FUNDS TO COMPLETE THIS TRANSACTION.\nPLEASE INSERT MORE MONEY.");
        }
        else
        {
            balance = balance - cost;
            String remainBal = String.valueOf(df.format(balance));
            jTextField6.setText(remainBal);
            double zero = 0.00;
            String zer = String.valueOf(df.format(zero));
            jTextField7.setText(zer);
            printToTextArea3("\n****************************************\nThank you for paying for this order."
                    + "\n\nYour business is greatly appreciated.\n\nPlease come again.\n\n****************************************");
        }   
    }//GEN-LAST:event_jButton29ActionPerformed

