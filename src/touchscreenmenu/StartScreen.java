    // Add Pizza to order
    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        ArrayList<PizzaToppings> topp = (ArrayList<PizzaToppings>) topping.getToppings().clone();
        String sizeOfPizza = jTextField3.getText();

        if(!jButton17.isEnabled() || !jButton18.isEnabled() || !jButton19.isEnabled()){
            if(sizeOfPizza.equalsIgnoreCase("LARGE  (VERY HUNGRY)"))
            {
                Pizza pizza = new Pizza();
                pizza = (new Pizza(PizzaSize.LARGE, "PIZZA", pizza.getLargeCost(), topp));
                order.addProduct(pizza);
                printToTextArea2(pizza.toString());
                printToTextArea1(order.toString());
                jButton20.setEnabled(false);
                jButton17.setEnabled(false);
                jButton18.setEnabled(false);
                jButton19.setEnabled(false);

            }
            else if(sizeOfPizza.equalsIgnoreCase("MEDIUM  (not as hungry)"))
            {
                Pizza pizza = new Pizza();
                pizza = new Pizza(PizzaSize.MEDIUM, "PIZZA", pizza.getMediumCost(), topping.getToppings());
                order.addProduct(pizza);
                printToTextArea2(pizza.toString());
                printToTextArea1(order.toString());
                jButton20.setEnabled(false);          
                jButton17.setEnabled(false);
                jButton18.setEnabled(false);
                jButton19.setEnabled(false);
            }
            else if(sizeOfPizza.equalsIgnoreCase("SMALL  (not so hungry)"))
            {
                Pizza pizza = new Pizza();
                pizza = new Pizza(PizzaSize.SMALL, "PIZZA", pizza.getSmallCost(), topping.getToppings());
                order.addProduct(pizza);
                printToTextArea2(pizza.toString());
                printToTextArea1(order.toString());
                jButton20.setEnabled(false);           
                jButton17.setEnabled(false);
                jButton18.setEnabled(false);
                jButton19.setEnabled(false);
            }   
            pizzajFrame1.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "You forgot to pick a size.");
        }
        
        
    }//GEN-LAST:event_jButton14ActionPerformed

